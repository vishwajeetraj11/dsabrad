const validSudoku = (board) => {
    const row = {};
    const col = {};
    const box = {};
    for(let i=0; i<9; i++) {
        for(let j=0; j<9; j++) {
            const value = board[i][j]
            if(value !== '.') {
                const boardIndex = Math.floor(i/3)*3+Math.floor(j/3);
                if(row[`${i}-${value}`] || col[`${j}-${value}`] || box[`${boardIndex}-${value}`]) {
                    return false;
                }
                row[`${i}-${value}`] = true;
                col[`${j}-${value}`] = true;
                box[`${boardIndex}-${value}`] = true;
            }
        }
    }
    return true;
}

